-- DIM_TIME
DROP TABLE IF EXISTS DIM_TIME CASCADE;
CREATE TABLE DIM_TIME (
	TIME_ID BIGINT PRIMARY KEY,
	TIME VARCHAR(20),
	DAY_OF_WEEK VARCHAR(20)
);

-- DIM_DRIVER
DROP TABLE IF EXISTS DIM_DRIVER CASCADE;
CREATE TABLE DIM_DRIVER (
	DRIVER_ID BIGINT PRIMARY KEY,
	AGE_BAND_OF_DRIVER VARCHAR(20),
	SEX_OF_DRIVER VARCHAR(20),
	EDUCATIONAL_LEVEL VARCHAR(50),
	VEHICLE_DRIVER_RELATION VARCHAR(20),
	DRIVING_EXPERIENCE VARCHAR(30)
);

-- DIM_CASUALTY
DROP TABLE IF EXISTS DIM_CASUALTY CASCADE;
CREATE TABLE DIM_CASUALTY (
	CASUALTY_ID BIGINT PRIMARY KEY,
	CASUALTY_CLASS VARCHAR(30),
	SEX_OF_CASUALTY VARCHAR(20),
	AGE_BAND_OF_CASUALTY VARCHAR(20),
	WORK_OF_CASUALTY VARCHAR(30),
	FITNESS_OF_CASUALTY VARCHAR(30)
);

-- DIM_VEHICLE
DROP TABLE IF EXISTS DIM_VEHICLE CASCADE;
CREATE TABLE DIM_VEHICLE (
	VEHICLE_ID BIGINT PRIMARY KEY,
	TYPE_OF_VEHICLE VARCHAR(50),
	OWNER_OF_VEHICLE VARCHAR(30),
	SERVICE_YEAR_OF_VEHICLE INTEGER,
	DEFECT_OF_VEHICLE VARCHAR(20),
	VEHICLE_MOVEMENT VARCHAR(50)
);

-- DIM_GEOGRAPHY
DROP TABLE IF EXISTS DIM_GEOGRAPHY CASCADE;
CREATE TABLE DIM_GEOGRAPHY (
	GEOGRAPHY_ID BIGINT PRIMARY KEY,
	AREA_ACCIDENT_OCCURRED VARCHAR(50)
);

-- DIM_ROAD_CHARACTERISTICS
DROP TABLE IF EXISTS DIM_ROAD_CHARACTERISTICS CASCADE;
CREATE TABLE DIM_ROAD_CHARACTERISTICS (
	ROAD_CHAR_ID BIGINT PRIMARY KEY,
	LANES_OR_MEDIANS VARCHAR(100),
	ROAD_ALIGNMENT VARCHAR(100),
	TYPES_OF_JUNCTION VARCHAR(30),
	ROAD_SURFACE_TYPE VARCHAR(50),
	ROAD_SURFACE_CONDITIONS VARCHAR(50)
);

-- DIM_ENVIRONMENT
DROP TABLE IF EXISTS DIM_ENVIRONMENT CASCADE;
CREATE TABLE DIM_ENVIRONMENT (
	ENVIRONMENT_ID BIGINT PRIMARY KEY,
	LIGHT_CONDITIONS VARCHAR(50),
	WEATHER_CONDITIONS VARCHAR(30)
);

-- DIM_ACCIDENT_EVENT
DROP TABLE IF EXISTS DIM_ACCIDENT_EVENT CASCADE;
CREATE TABLE DIM_ACCIDENT_EVENT (
	ACCIDENT_EVENT_ID BIGINT PRIMARY KEY,
	TYPE_OF_COLLISION VARCHAR(100),
	PEDESTRIAN_MOVEMENT VARCHAR(200),
	CAUSE_OF_ACCIDENT VARCHAR(100)
);

-- FACT_ACCIDENT
DROP TABLE IF EXISTS FACT_ACCIDENT CASCADE;
CREATE TABLE FACT_ACCIDENT (
	ACCIDENT_ID BIGINT PRIMARY KEY,
	TIME_ID BIGINT,
	DRIVER_ID BIGINT,
	VEHICLE_ID BIGINT,
	GEOGRAPHY_ID BIGINT,
	ROAD_CHAR_ID BIGINT,
	ENVIRONMENT_ID BIGINT,
	ACCIDENT_EVENT_ID BIGINT,
	NUMBER_OF_VEHICLES_INVOLVED DOUBLE PRECISION,
	NUMBER_OF_CASUALTIES DOUBLE PRECISION,
	ACCIDENT_SEVERITY VARCHAR(30),
	FOREIGN KEY (TIME_ID) REFERENCES DIM_TIME (TIME_ID),
	FOREIGN KEY (DRIVER_ID) REFERENCES DIM_DRIVER (DRIVER_ID),
	FOREIGN KEY (VEHICLE_ID) REFERENCES DIM_VEHICLE (VEHICLE_ID),
	FOREIGN KEY (GEOGRAPHY_ID) REFERENCES DIM_GEOGRAPHY (GEOGRAPHY_ID),
	FOREIGN KEY (ROAD_CHAR_ID) REFERENCES DIM_ROAD_CHARACTERISTICS (ROAD_CHAR_ID),
	FOREIGN KEY (ENVIRONMENT_ID) REFERENCES DIM_ENVIRONMENT (ENVIRONMENT_ID),
	FOREIGN KEY (ACCIDENT_EVENT_ID) REFERENCES DIM_ACCIDENT_EVENT (ACCIDENT_EVENT_ID)
);

-- BRIDGE_ACCIDENT_CASUALTY
DROP TABLE IF EXISTS BRIDGE_ACCIDENT_CASUALTY CASCADE;
CREATE TABLE BRIDGE_ACCIDENT_CASUALTY (
	BRIDGE_ACCIDENT_CASUALTY_ID BIGINT PRIMARY KEY,
	ACCIDENT_ID BIGINT,
	CASUALTY_ID BIGINT,
	CASUALTY_SEVERITY BIGINT,
	FOREIGN KEY (ACCIDENT_ID) REFERENCES FACT_ACCIDENT (ACCIDENT_ID),
	FOREIGN KEY (CASUALTY_ID) REFERENCES DIM_CASUALTY (CASUALTY_ID)
);
